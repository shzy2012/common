package tools

import (
	"fmt"
	"testing"
)

func TestDetectLanguage(t *testing.T) {

	examples := []string{
		"銀行",                   // 中文 (繁体)
		"你好，世界",                // 中文 (简体)
		"こんにちは",                // 日语 (平假名)
		"最新ニュース",               // 日语 (汉字+片假名)
		"안녕하세요",                // 韩语 (谚文)
		"한국어 contains 汉字",      // 韩语 (谚文+汉字)
		"Hello World",          // 英文
		"C'est la vie",         // 法文
		"Bonjour à tous",       // 法文
		"Привет, мир",          // 俄文
		"مرحبا بالعالم",        // 阿文
		"简体中文程序",               // 中文 (简体)
		"繁體中文程序",               // 中文 (繁体)
		"義大利麵",                 // 中文 (繁体)
		"这只是一个简单的测试。",          // 中文 (简体)
		"這只是一個簡單的測試。",          // 中文 (繁体)
		"東京は美しいです",             // 日语
		"Go语言是一种静态、并发型语言。",     // 中英混排 (简体)
		"Go語言是一種靜態、併發型語言。",     // 中英混排 (繁体)
		"L'été arrive bientôt", // 法文
		"Je m'appelle Lucy.",   // 法文
		"1234567890",           // 纯数字
		"!!!??? @#$%",          // 纯符号
		"汉字とひらがな",              // 中日混合
		"한글과 漢字",               // 中韩混合
	}

	for _, ex := range examples {
		fmt.Printf("文本: %-30s -> 判定结果: %s\n", ex, DetectLanguage(ex))
	}
}
